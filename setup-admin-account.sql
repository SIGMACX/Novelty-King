-- 管理员账号设置脚本
-- 请按照以下步骤在 Supabase SQL Editor 中执行

-- ========================================
-- 第一步：创建管理员邮箱账户
-- ========================================

-- 方法 A：使用邮箱 OTP 注册（推荐）
-- 1. 访问 http://localhost:3001/admin/login
-- 2. 输入您想要设置为管理员的邮箱（例如：admin@example.com）
-- 3. 点击"发送验证码"
-- 4. 检查邮箱并输入验证码
-- 5. 此时会提示"您没有管理员权限"（这是正常的，因为还没有设置）

-- 方法 B：在 Supabase Authentication 中手动创建用户
-- 1. 在 Supabase 控制台，点击 Authentication -> Users
-- 2. 点击 "Add user" -> "Create new user"
-- 3. 输入邮箱和密码
-- 4. 点击 "Create user"

-- ========================================
-- 第二步：查找管理员用户的 ID
-- ========================================

-- 执行以下查询，找到您刚才注册的管理员邮箱对应的用户 ID
SELECT id, email, created_at
FROM auth.users
WHERE email = 'admin@example.com';  -- 替换为您的管理员邮箱

-- 复制返回结果中的 ID（一串类似 "123e4567-e89b-12d3-a456-426614174000" 的 UUID）

-- ========================================
-- 第三步：将用户添加到管理员表
-- ========================================

-- 将下面的 '您的用户ID' 替换为第二步中复制的 UUID
INSERT INTO admin_users (user_id)
VALUES ('您的用户ID');

-- 示例（请替换为实际的用户 ID）：
-- INSERT INTO admin_users (user_id)
-- VALUES ('123e4567-e89b-12d3-a456-426614174000');

-- ========================================
-- 第四步：验证管理员设置成功
-- ========================================

-- 执行以下查询，确认管理员已添加成功
SELECT
  au.user_id,
  u.email,
  au.created_at as admin_since
FROM admin_users au
JOIN auth.users u ON au.user_id = u.id;

-- 应该能看到刚才添加的管理员邮箱

-- ========================================
-- 第五步：测试管理员登录
-- ========================================

-- 1. 访问 http://localhost:3001/admin/login
-- 2. 输入管理员邮箱
-- 3. 点击"发送验证码"
-- 4. 输入验证码
-- 5. 应该会提示"管理员登录成功！正在跳转..."
-- 6. 自动跳转到管理后台 http://localhost:3001/admin

-- ========================================
-- 可选：移除管理员权限
-- ========================================

-- 如果需要移除某个用户的管理员权限，执行以下语句：
-- DELETE FROM admin_users WHERE user_id = '要移除的用户ID';

-- ========================================
-- 可选：查看所有管理员
-- ========================================

-- 执行以下查询查看系统中所有的管理员账户
SELECT
  au.user_id,
  u.email,
  au.created_at as admin_since
FROM admin_users au
JOIN auth.users u ON au.user_id = u.id
ORDER BY au.created_at DESC;

-- ========================================
-- 常见问题
-- ========================================

-- Q1: 执行 INSERT 时提示 "duplicate key value violates unique constraint"
-- A1: 该用户已经是管理员了，无需重复添加

-- Q2: 执行 INSERT 时提示 "violates foreign key constraint"
-- A2: 用户 ID 不存在，请检查第二步的查询结果，确保使用正确的用户 ID

-- Q3: 登录后提示"您没有管理员权限"
-- A3: 检查是否成功执行了第三步的 INSERT 语句，并确认用户 ID 正确

-- Q4: 如何创建多个管理员？
-- A4: 重复执行第一到第三步，为每个管理员邮箱添加记录

-- ========================================
-- 安全提示
-- ========================================

-- 1. 管理员账户应使用强密码（虽然使用 OTP 验证）
-- 2. 定期检查管理员列表，移除不再需要的管理员权限
-- 3. 管理员邮箱应该是可信任的邮箱地址
-- 4. 不要在公开环境中分享管理员邮箱
